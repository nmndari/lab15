package labxx.sict.must.edu.mn;

/**
 * Үржүүлэх үйлдлийг хийдэг класс.
 */
public class Multiplication {

    /**
     * Хоёр тоог үржүүлнэ.
     *
     * @param a эхний тоо (final)
     * @param b хоёр дахь тоо (final)
     * @return үржвэр
     */
    public double multiply(final double a, final double b) {
        return a * b;
    }
}
